#include <stdio.h>

int main()
{
	printf("The integer value of the ascii code '%c' is %d\n", 21, '§');
	printf("The integer value of the ascii code '%%' is %d\n", '%');
	printf("The integer value of the ascii code '!' is %d\n", '!');
	printf("The integer value of the ascii code '2' is %d\n", '2');
	printf("The integer value of the ascii code 'i' is %d\n", 'i');
	printf("The integer value of the ascii code 'I' is %d\n", 'I');
	printf("The integer value of the ascii code 'a' is %d\n", 'a');
	printf("The integer value of the ascii code 'A' is %d\n", 'A');
	printf("The integer value of the ascii code '=' is %d\n", '=');
	printf("The integer value of the ascii code ']' is %d\n", ']');
	
	return 0;
}
